a = 10
b = 3

print(f'A soma das variáveis a e b resulta em: {a + b}')
print(f'A subtração da variável b de a resulta em: {a - b}')
print(f'A multiplicação da variável a por b resulta em: {a * b}')
print(f'A divisão da variável a por b resulta em: {a / b:.2f}')
print(f'A divisão inteira da variável a por b resulta em: {a // b}')